#직접 데이터를 넣어 초기화
a = [1,2,3,4,5,6,7,8,9]
print(a)

#네 번째 원소만 출력
print(a[3])

#네 번째 원소를 44로 변경
a[3]=44
print(a)

#크키가 n이고, 모든 값이 0인 1차 리스트 초기화
a = [0]*10
print(a)
